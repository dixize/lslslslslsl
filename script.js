function showSlide(key) {
    const data = slidesData[key];
    const content = `
        <h1 style="color: #001a4d; border-bottom: 4px solid #ff4f12; padding-bottom: 10px;">${data.title}</h1>
        <div style="margin: 30px 0;">
            <h3>Предпосылки развития:</h3>
            <p style="font-size: 1.1rem; background: #f9f9f9; padding: 20px; border-radius: 10px;">${data.pre}</p>
        </div>
        
        <h3>Область применения:</h3>
        <div class="slide-grid">
            ${data.photos.map(ph => `
                <div class="slide-photo-card">
                    <img src="${ph.img}" alt="${ph.t}">
                    <h4>${ph.t}</h4>
                    <p style="padding: 0 10px; font-size: 0.9rem;">${ph.d}</p>
                </div>
            `).join('')}
        </div>

        <div style="background: #001a4d; color: white; padding: 30px; border-radius: 15px;">
            <h3>Эффекты:</h3>
            <p style="font-size: 1.2rem;">${data.effects}</p>
        </div>

        <h3>Опыт внедрения:</h3>
        <div class="partners-block">
            ${data.partners.map(p => `<div style="padding: 10px 20px; background: white; border-radius: 5px; font-weight: bold; border: 1px solid #ddd;">${p}</div>`).join('')}
        </div>
    `;

    document.getElementById('slide-content').innerHTML = content;
    document.getElementById('slide-container').classList.add('visible');
    document.body.style.overflow = 'hidden';
}

function hideSlide() {
    document.getElementById('slide-container').classList.remove('visible');
    document.body.style.overflow = 'auto';
}
